select w.SUBWORKTASK_TITLE,  u.NAME, w.SUBWORKTASK_DATEREADY, w.SUBWORKTASK_DATEDONE, ROUND(w.SUBWORKTASK_DATEDONE - w.SUBWORKTASK_DATEREADY,2) AS TOTALDAY, w.SUBWORKTASK_STATUS, FCN_WF_GET_COMMENT(a.workaudit_workid,a.WORKAUDIT_TASKID) as Comentario
from wworkaudit a, wsubworktask w, vw_user u
where  a.workaudit_workid = w.SUBWORKTASK_SUBWORKID
and a.WORKAUDIT_TASKID = w.SUBWORKTASK_TASKID
and w.SUBWORKTASK_PERFORMERID = u.id
and a.WORKAUDIT_STATUS in (29)
and w.SUBWORKTASK_FLAGS = 36
and w.SUBWORKTASK_STATUS < 0
and a.WORKAUDIT_TASKID = 18
and w.SUBWORKTASK_TITLE = 'TB EHS Evaluation'
order by TOTALDAY desc

